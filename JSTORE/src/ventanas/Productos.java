    public final void MostrarProductos() {
    try {
            DefaultTableModel modelo = (DefaultTableModel) jTableProducto.getModel();
            Controlador c = new Controlador();
            List<Producto> productos = c.mostrarProductos();
            for (Producto producto : productos) {
                Object datos[] = {producto.getCodProducto(),producto.getPresentacion()
                ,producto.getCantidad(),producto.getCosto(),producto.getPrecio(),producto.getFecha()};
                modelo.addRow(datos);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay conexion a la base de datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

    void LimpiarJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableProducto.getModel();
        try {
            int a = modelo.getRowCount();
            for (int i = a - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        } catch (Exception e) {
            System.out.println("error.....");
        }
    }


